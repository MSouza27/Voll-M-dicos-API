package br.com.exercicio.voll.medico;

// Declaração de um enum chamado "Especialidade"
// Enums são usados para representar um conjunto fixo de constantes, como os valores possíveis de uma especialidade médica
public enum Especialidade {

    // Define as constantes do enum, representando especialidades médicas
    ORTOPEDIA,    // Especialidade médica que trata problemas nos ossos, músculos, ligamentos e articulações
    CARDIOLOGIA,  // Especialidade que cuida do sistema cardiovascular (coração e vasos sanguíneos)
    GERIATRIA,    // Especialidade focada no cuidado de pacientes idosos
    NEUROLOGIA,   // Especialidade médica que trata do sistema nervoso central e periférico
    PEDIATRIA     // Especialidade voltada para a saúde de bebês, crianças e adolescentes
    // Note que não é necessário ponto-e-vírgula após o último elemento em um enum
}

